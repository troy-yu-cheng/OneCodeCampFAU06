// Primitive Data Types

// String: textual data that is enclosed in "" or ''
let firstName = "John";
console.log("First name:", firstName)
console.log("Type of First Name:", typeof firstName)

// Number: Represents nunmeric value (e.g 30, -30, 3,14)
let age = 30
console.log("Age", age);
console.log("Type of Age:", typeof age)

// Boolean: Either be true of false
let isStudent = false;
console.log("Is user a student:", isStudent);
console.log("Type of isStudent:", typeof isStudent);

// Null: International absence of value
let car = null;
console.log("Car:", car);
console.log("Type of car variable:", typeof car);

// Undefined: Unintentional absence of value
let city;
console.log("City:", city)
console.log("Type of city varible:", typeof city)